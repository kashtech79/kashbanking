package com.kash.entity;

import com.fasterxml.jackson.annotation.JsonBackReference;
import lombok.Getter;
import lombok.Setter;

import javax.persistence.*;
import java.math.BigDecimal;
import java.util.Date;

@Entity
@Getter
@Setter
public class CurrentTransactions {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    private Date date;
    private String description;
    private String type;
    private String status;
    private double amount;
    private BigDecimal availableBalance;
    @ManyToOne
    @JoinColumn(name = "current_account_id")
    @JsonBackReference
    private CurrentAccount currentAccount;

    public CurrentTransactions() {
    }

    public CurrentTransactions(Date date, String description, String type, String status, double amount, BigDecimal availableBalance, CurrentAccount currentAccount) {
        this.date = date;
        this.description = description;
        this.type = type;
        this.status = status;
        this.amount = amount;
        this.availableBalance = availableBalance;
        this.currentAccount = currentAccount;
    }
}
